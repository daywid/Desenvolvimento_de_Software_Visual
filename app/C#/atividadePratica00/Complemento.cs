public class Complemento
{
    private int _id
    private String _descricao;
    private double _valor;

    public int Id
    {
        get { return _id; }
        set { _id = value; }
    }

    public String Descricao
    {
        get { return _descricao;}
        set { _descricao = value;}
    }

    public double Valor
    {
        get { return _valor; }
        set { _valor = value; }
    }
}